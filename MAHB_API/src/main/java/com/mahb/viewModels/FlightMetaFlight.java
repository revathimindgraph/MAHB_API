package com.mahb.viewModels;

public class FlightMetaFlight {

	public String flightNumber;
	public String leg ;
	public String airCraftOperatorCode;
	public String originCity ;
	public String destinationCity ;

	public String getFlightNumber() {
		return flightNumber;
	}
	public void setFlightNumber(String flightNumber) {
		this.flightNumber = flightNumber;
	}
	public String getLeg() {
		return leg;
	}
	public void setLeg(String leg) {
		this.leg = leg;
	}
	public String getAirCraftOperatorCode() {
		return airCraftOperatorCode;
	}
	public void setAirCraftOperatorCode(String airCraftOperatorCode) {
		this.airCraftOperatorCode = airCraftOperatorCode;
	}
	public String getOriginCity() {
		return originCity;
	}
	public void setOriginCity(String originCity) {
		this.originCity = originCity;
	}
	public String getDestinationCity() {
		return destinationCity;
	}
	public void setDestinationCity(String destinationCity) {
		this.destinationCity = destinationCity;
	}


}
